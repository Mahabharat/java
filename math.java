class math
{
	public static void main(String args[])
	{
		System.out.println(Math.abs(-24.3));
		System.out.println(Math.ceil(24.3));
		System.out.println(Math.floor(24.3));
		System.out.println(Math.max(24.3,14.9));
		System.out.println(Math.min(24.3,14.9));
		System.out.println(Math.pow(5,3));
		System.out.println(Math.sqrt(144));
	}
}

