class switc
{
	public static void main(String args[])
	{
		int age=21;
		switch(age)
		{
			case 21:
				System.out.println("you are mature");
				break;
			case 18:
				System.out.println("you are immature");
				break;
			case 55:
				System.out.println("you are too old");
				break;
			default:
				System.out.println("you age is not known");
				break;
		}
	}
}
