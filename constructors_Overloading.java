class constructors_Overloading
{
	public static void main(String args[])
	{
		Overload obj=new Overload();
		obj.display();
		Overload obj1=new Overload(23);
		obj1.display();
		Overload obj2=new Overload(12,45);
		obj2.display();
		Overload obj3=new Overload(10,23,45);
		obj3.display();
	}
}
		
		
